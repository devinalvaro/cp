#include <vector>

using namespace std;

int main() {
    int n;
    cin >> n;

    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }

    sort(a.begin(), a.end());

    long long change = 0;
    for (int i = n - 1; i >= 0; i--) {
        change += abs((i + 1) - a[i]);
    }

    cout << change << endl;
}
